// ตัดลูกน้ำก่อน save
function cutComma($param1)
{
    $old_str = $param1;
    $new_str = str_replace(',', '', $old_str);

    return "$new_str";
}

// ตัดเครื่องหมายขีด
function cutDash($param1)
{
    $old_str = $param1;
    $new_str = ltrim($old_str, '-');

    return "$new_str";
}
